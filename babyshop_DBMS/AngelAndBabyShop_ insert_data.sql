SET discount = Product.discount
FROM Product INNER JOIN Order_Details ON Product.id = Order_Details. product_id

update Order_Details 
SET Order_Details. price = CASE
		WHEN P.discount = 0 then P.outbound_price
		ELSE P.outbound_price - P.discount * P.outbound_price
		END
FROM Product AS P
WHERE P.id = Order_Details.product_id


--cập nhật dữ liệu total_money bảng Orders
UPDATE Orders
SET total_money = subquery.total_money
FROM Orders
	INNER JOIN(
    SELECT 
        O.id,
        SUM(OD.price * OD.num) AS total_money
    FROM 
        Orders AS O
    JOIN 
        Order_Details AS OD ON O.id = OD.order_id
    GROUP BY 
        O.id
) AS subquery ON Orders.id = subquery.id;

